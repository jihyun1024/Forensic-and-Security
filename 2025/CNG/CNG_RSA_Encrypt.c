// Encryption Function
void Encrypt()
{
	NTSTATUS status = 0;
	BYTE Plain[] = { 
		'F', 'a', 'S', ' ', 
		'2', '0', '2', '5', 
		' ', 'N', 'i', 'c', 
		'e', ' ', 'H', 'a', 
		'p', 'p', 'y', ' ', 
		'G', 'o', 'o', 'd', '~', '\0' };

	BYTE* Cipher = NULL;
	DWORD CipherLength = 0;
	DWORD bufferSize = 0;

	// CNG setting
	BCRYPT_ALG_HANDLE algHandle = BCRYPT_RSA_ALG_HANDLE;
	BCRYPT_KEY_HANDLE PublicKey_Handle = NULL;

	// Set Public Key BLOB
	PublicKey_BLOB* PublicKey = NULL;
	PublicKey = (PublicKey_BLOB*)calloc(1, sizeof(PublicKey_BLOB));
	if (PublicKey == NULL) { printf("Memory Allocation Error\n"); return; }

	PublicKey->Magic = BCRYPT_RSAPUBLIC_MAGIC;
	PublicKey->BitLength = 4096;
	PublicKey->cbPublicExp = 3;
	PublicKey->cbModulus = 512;
	PublicKey->cbPrime1 = 0;
	PublicKey->cbPrime2 = 0;
	memcpy(PublicKey->PublicExponent, e, 3);
	memcpy(PublicKey->Modulus, n, 512);

	// Import Key Pair
	status = BCryptImportKeyPair(
		algHandle,
		NULL,
		BCRYPT_RSAPUBLIC_BLOB,
		&PublicKey_Handle,
		(PBYTE)PublicKey,
		RSA_PublicKey_BLOB_Size,
		BCRYPT_NO_KEY_VALIDATION);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Import Key Pair: %x\n", status);
		return;
	}

	// Calculate Ciphertext Length
	status = BCryptEncrypt(
		PublicKey_Handle,
		Plain,
		sizeof(Plain),
		NULL,
		NULL,
		0,
		NULL,
		0,
		&CipherLength,
		BCRYPT_PAD_PKCS1
	);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Calculate Ciphertext Length: %x\n", status);
		return;
	}

	// Operate Encryption
	Cipher = (PBYTE)calloc(CipherLength, sizeof(BYTE));
	if (Cipher == NULL) return;
	status = BCryptEncrypt(
		PublicKey_Handle,
		Plain,
		sizeof(Plain),
		NULL,
		NULL,
		0,
		Cipher,
		CipherLength,
		&bufferSize,
		BCRYPT_PAD_PKCS1);
	if (!NT_SUCCESS(status)) {
		printf("Failed to Encrypt Plaintext: %x\n", status);
		return;
	}

	// Print Ciphertext
	CipherLength = bufferSize;
	printf("Ciphertext: ");
	for (int cnt_i = 0; cnt_i < CipherLength; cnt_i++) {
		if (cnt_i % 16 == 0) printf("\n");
		if (cnt_i == CipherLength - 1) {
			printf("0x%02x", Cipher[cnt_i]);
			break;
		}
		printf("0x%02x, ", Cipher[cnt_i]);
	}
	printf("\nCiphertext Size: %d Bytes\n", CipherLength);

	// Destroy Key and Close Algorithm Provider
	BCryptDestroyKey(PublicKey_Handle);
	BCryptCloseAlgorithmProvider(algHandle, 0);
	free(Cipher);
	free(PublicKey);
}

int main()
{
	Encrypt();

	return 0;
}
